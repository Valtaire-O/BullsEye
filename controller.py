class ImageFinder:

    def __init__(self):
        self.settings ={
            'BOT_NAME': 'bulls_eye',
            'NEWSPIDER_MODULE': 'bulls_eye.spiders',
            'REQUEST_FINGERPRINTER_IMPLEMENTATION': '2.7',
            'SPIDER_MODULES':  ['bulls_eye.bulls_eye.spiders'],
            'SCRAPFLY_API_KEY': 'e5e77e603ae744b0aa408e4f0b59cd61',
            'SCRAPFLY_CUSTOM_RETRY_CODE': ['ERR::PROXY::UNAVAILABLE',
                                           'ERR::SCRAPE::BAD_UPSTREAM_RESPONSE'],
            'CONCURRENT_REQUESTS': 20
        }
    def start(self, links:list):
        # empty out data sets
        js = open('bulls_eye/bulls_eye/spiders/site_data/check_images.json', "w")
        js.write(json.dumps({"Row": []}, indent=2, sort_keys=True))
        js.close()
        js = open('bulls_eye/bulls_eye/spiders/site_data/start_set.json', "w")
        js.write(json.dumps({"Row": []}, indent=2, sort_keys=True))
        js.close()

        validate = validations()
        keep,toss = [],[]
        count = 0
        already_seen = Counter()

        for l in links:
            if already_seen[l] !=0:
                continue

            already_seen[l] = 1
            count +=1
            confirm_url = validate.confirm_url(l)
            if confirm_url:
                keep.append({"url":confirm_url,"id":count})
                continue
            toss.append({"url":l,"id":count})

        js = open('bulls_eye/bulls_eye/spiders/site_data/rejected.json', "w")
        js.write(json.dumps({"Row": toss}, indent=2, sort_keys=True))
        js.close()

        js = open('bulls_eye/bulls_eye/spiders/site_data/start_set.json', "w")
        js.write(json.dumps({"Row": keep}, indent=2, sort_keys=True))
        js.close()

        self._start_img_spider()




    def _start_img_spider(self):

        from bulls_eye.bulls_eye.spiders.get_response import BullsEye

        process = CrawlerProcess(settings=self.settings)
        process.crawl(BullsEye)
        start = perf_counter()
        process.start()
        stop = perf_counter()
        self.show_results()




    def show_results(self):
        """return and print out results on all links"""

        with open('bulls_eye/bulls_eye/spiders/site_data/bad_request.json') as f:
            bad_req = json.load(f)["Row"]

        with open('bulls_eye/bulls_eye/spiders/image_data/none_found.json') as f:
            none_found = json.load(f)["Row"]

        with open('bulls_eye/bulls_eye/spiders/image_data/found.json') as f:
            found_images = json.load(f)["Row"]

        report ={"found":[],"none_found":[],"bad_request":[],}

        for f in found_images:
            if 'meta_block' and 'image_block' in f.keys():
                del f['meta_block']
                del f['image_block']
            if 'possible_images' in f.keys():
                del f['possible_images']
            report["found"].append(f)

        for n in none_found:
            if 'meta_block' and 'image_block' in n.keys():
                del n['meta_block']
                del n['image_block']
            if 'possible_images' in n.keys():
                del n['possible_images']
            report["none_found"].append(n)


        for b in bad_req:
            report["bad_request"].append(b)

        print(json.dumps(report,indent=2))
        return report










